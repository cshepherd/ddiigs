  hex 00000000

*----------------------------------------------------------
* half_sec - Delay for 30 VBLs (~0.5 seconds at 60Hz)
* Assumes native mode with 16-bit X/Y.
*----------------------------------------------------------
quarter_sec
  rep $30               ; assert 16-bit for assembler MX tracking
  ldx #15
:loop phx
  jsr wait_for_vbl
  plx
  dex
  bne :loop
  rts

